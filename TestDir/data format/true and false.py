# True and False ( Boolean )

if("Python"): # true
    print("true")
else:
    print("false")

if(""): # false
    print("true")
else:
    print("false")

if([1,2,3]): # true
    print("true")
else:
    print("false")

if([]): # false
    print("true")
else:
    print("false")

if(()): # false
    print("true")
else:
    print("false")

if({}): # false
    print("true")
else:
    print("false")

if(1): # true
    print("true")
else:
    print("false")

if(0): # false
    print("true")
else:
    print("false")

if(None): # false
    print("true")
else:
    print("false")

